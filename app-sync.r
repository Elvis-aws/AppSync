
********
App-Sync
********
    - AWS AppSync provides a robust, scalable GraphQL interface for application developers to combine data from
       multiple sources, including Amazon DynamoDB, AWS Lambda, and HTTP APIs
    ****************
    AppSync features
    ****************
            - Powerful GraphQL schema editing through the AWS AppSync console, including automatic
              GraphQL schema generation from DynamoDB
            - Support of JavaScript and TypeScript to write business logic that accesses your data sources
               Efficient data caching
            - Integration with Amazon Cognito user pools for fine-grained access control at a per-field level
